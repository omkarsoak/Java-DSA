public class print_array_elements 
{
    public static void main(String[] args)
    {
        int[] numbers ={1,2,3,4,5};
        for(int x: numbers)
        {
            System.out.println(x);
        }

        int i=0;
        while(i<=10)
        {
            System.out.println(i);
            i++;
        }

    }
}
